// scripts/rewind-zcta-geometry.mjs
//
// One-shot repair: rewinds polygon ring orientation in
// functions/sandbox/data/zcta_geometry.js so d3.geoPath (spherical
// winding) treats the small ZCTA polygon as the inside of the shape
// instead of the entire sphere minus the polygon. The original
// build-zcta.mjs run wrote RFC-7946 GeoJSON straight from TIGERweb,
// which uses the opposite convention and caused every ZCTA path to
// render a viewBox-sized rectangle covering the whole map.
//
// Re-running build-zcta.mjs would refetch ~33 K polygons from the
// Census TIGERweb MapServer, so this script just reverses rings in
// place. build-zcta.mjs itself was updated to apply the same rewind
// step on future runs.
//
// Run with:  node scripts/rewind-zcta-geometry.mjs

import { writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { ZCTA_GEOJSON } from '../functions/sandbox/data/zcta_geometry.js';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = resolve(__dirname, '..', 'functions/sandbox/data/zcta_geometry.js');

function ringSignedArea(ring) {
  let a = 0;
  for (let i = 0; i < ring.length - 1; i++) {
    a += ring[i][0] * ring[i + 1][1] - ring[i + 1][0] * ring[i][1];
  }
  return a / 2;
}
function rewindPolygon(rings) {
  return rings.map((ring, i) => {
    const isExterior = i === 0;
    const isCW = ringSignedArea(ring) < 0;
    return isExterior === isCW ? ring : ring.slice().reverse();
  });
}
function rewindGeometry(g) {
  if (!g) return g;
  if (g.type === 'Polygon') return { type: 'Polygon', coordinates: rewindPolygon(g.coordinates) };
  if (g.type === 'MultiPolygon') return { type: 'MultiPolygon', coordinates: g.coordinates.map(rewindPolygon) };
  return g;
}

let flipped = 0;
const features = ZCTA_GEOJSON.features.map(f => {
  const before = JSON.stringify(f.geometry.coordinates);
  const geometry = rewindGeometry(f.geometry);
  if (JSON.stringify(geometry.coordinates) !== before) flipped++;
  return { type: 'Feature', properties: f.properties, geometry };
});
console.log(`rewound ${flipped} of ${features.length} features`);

const fc = { type: 'FeatureCollection', features };
await writeFile(OUT, `// Auto-generated by scripts/build-zcta.mjs.
// DO NOT EDIT — re-run the script to refresh.
// 2020 Census ZIP Code Tabulation Areas (~33K), simplified to
// ~6km tolerance for national-scale rendering.
// Properties: { z: 5-digit ZCTA code }.
// Source: TIGERweb MapServer (PUMA_TAD_TAZ_UGA_ZCTA layer 1).

export const ZCTA_GEOJSON = ${JSON.stringify(fc)};
`);
console.log(`wrote ${OUT}`);
