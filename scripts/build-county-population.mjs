// scripts/build-county-population.mjs
//
// One-shot build for the "Population over time" layer on /sandbox/us-map.
// Pulls Van Leuven's "Historical U.S. County Population Data: 1900 to
// 2020" CSV (decennial counts per county, 1900–2020) and reshapes it
// into the time-series format the platform expects.
//
// Source: Andrew Van Leuven, Harvard Dataverse
//   https://doi.org/10.7910/DVN/WLS5GF
//   Direct CSV: https://andrewvanleuven.com/files/data/historical_county_populations_v2.csv
//
// Run with:  node scripts/build-county-population.mjs

import { writeFile, readFile, mkdir } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { dirname, resolve, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { tmpdir } from 'node:os';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');
const OUT_JSON = resolve(REPO_ROOT, 'functions/sandbox/data/county_population.json');
const OUT_JS   = resolve(REPO_ROOT, 'functions/sandbox/data/county_population.js');

const CSV_URL = 'https://andrewvanleuven.com/files/data/historical_county_populations_v2.csv';
const CACHE = join(tmpdir(), 'historical_county_populations_v2.csv');

async function fetchCsv() {
  if (existsSync(CACHE)) {
    return readFile(CACHE, 'utf8');
  }
  console.log('Downloading', CSV_URL);
  const res = await fetch(CSV_URL);
  if (!res.ok) throw new Error('CSV fetch failed: ' + res.status);
  const txt = await res.text();
  await writeFile(CACHE, txt);
  return txt;
}

// Tiny CSV parser — the file is regular: each row has 15 fields, one
// quoted "County, State" name, no embedded quotes. A 4-line state
// machine is overkill; a regex split that respects quotes is enough.
function splitCsvLine(line) {
  const out = [];
  let cur = '';
  let inQ = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (c === '"') { inQ = !inQ; continue; }
    if (c === ',' && !inQ) { out.push(cur); cur = ''; continue; }
    cur += c;
  }
  out.push(cur);
  return out;
}

async function main() {
  const txt = await fetchCsv();
  const lines = txt.split(/\r?\n/).filter(l => l.length > 0);
  const header = splitCsvLine(lines[0]);
  const popCols = [];  // [{ year, idx }]
  for (let i = 0; i < header.length; i++) {
    const m = header[i].match(/^pop_(\d{4})$/);
    if (m) popCols.push({ year: parseInt(m[1], 10), idx: i });
  }
  popCols.sort((a, b) => a.year - b.year);
  const years = popCols.map(c => c.year);
  const fipsIdx = header.indexOf('cty_fips');
  console.log('Years:', years.join(', '));

  const data = {};
  let rows = 0;
  for (let li = 1; li < lines.length; li++) {
    const cols = splitCsvLine(lines[li]);
    const fips = (cols[fipsIdx] || '').trim().padStart(5, '0');
    if (!/^\d{5}$/.test(fips)) continue;
    const arr = popCols.map(({ idx }) => {
      const v = parseInt(cols[idx], 10);
      return Number.isFinite(v) ? v : null;
    });
    data[fips] = arr;
    rows++;
  }
  console.log(`Parsed ${rows} counties × ${years.length} decennial years`);

  const meta = {
    source: CSV_URL,
    citation: 'Van Leuven, Andrew J, 2024 — Historical U.S. County Population Data: 1900 to 2020 (Harvard Dataverse, doi:10.7910/DVN/WLS5GF)',
    years,
    units: 'people',
    generated: new Date().toISOString().slice(0, 10),
  };

  await mkdir(dirname(OUT_JSON), { recursive: true });
  await writeFile(OUT_JSON, JSON.stringify({ __meta: meta, years, data }, null, 0) + '\n');
  console.log(`Wrote ${OUT_JSON}`);

  const js = `// Auto-generated by scripts/build-county-population.mjs.
// DO NOT EDIT by hand — re-run the script to refresh.
// Decennial population per US county FIPS, 1900-2020.
// COUNTY_POPULATION_YEARS[i] aligns with each county's
// COUNTY_POPULATION[fips][i].
//
// Source: ${CSV_URL}
//   (Van Leuven, Harvard Dataverse, doi:10.7910/DVN/WLS5GF)

export const COUNTY_POPULATION_YEARS = ${JSON.stringify(years)};
export const COUNTY_POPULATION = ${JSON.stringify(data)};
`;
  await writeFile(OUT_JS, js);
  console.log(`Wrote ${OUT_JS}`);
}

main().catch(err => { console.error(err); process.exit(1); });
