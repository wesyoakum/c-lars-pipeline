// scripts/build-county-income.mjs
//
// One-shot build for the "Median Household Income" layer on
// /sandbox/us-map. Pulls B19013_001E (median household income, past
// 12 months in inflation-adjusted dollars) from the Census ACS 5-year
// estimates for every county in one API call. No key required.
//
// Run with:  node scripts/build-county-income.mjs

import { writeFile, mkdir } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');
const OUT_JSON = resolve(REPO_ROOT, 'functions/sandbox/data/county_median_income.json');
const OUT_JS   = resolve(REPO_ROOT, 'functions/sandbox/data/county_median_income.js');

const ACS_YEAR = 2022;  // most recent ACS 5-year (2018-2022) at the time of writing
const ACS_URL  = `https://api.census.gov/data/${ACS_YEAR}/acs/acs5?get=NAME,B19013_001E&for=county:*`;

async function main() {
  console.log('GET', ACS_URL);
  const res = await fetch(ACS_URL);
  if (!res.ok) throw new Error(`ACS fetch failed: ${res.status}`);
  const rows = await res.json();
  // First row is the header.
  const header = rows[0];
  const iName  = header.indexOf('NAME');
  const iVal   = header.indexOf('B19013_001E');
  const iSt    = header.indexOf('state');
  const iCo    = header.indexOf('county');

  const out = {};
  let lo = Infinity, hi = -Infinity, n = 0;
  for (let i = 1; i < rows.length; i++) {
    const r = rows[i];
    const fipsState = String(r[iSt]).padStart(2, '0');
    const fipsCounty = String(r[iCo]).padStart(3, '0');
    const fips = fipsState + fipsCounty;
    const v = parseInt(r[iVal], 10);
    if (!Number.isFinite(v) || v <= 0) continue;
    out[fips] = v;
    if (v < lo) lo = v;
    if (v > hi) hi = v;
    n++;
  }
  console.log(`Parsed ${n} counties; range $${lo.toLocaleString()} - $${hi.toLocaleString()}`);

  const meta = {
    source: ACS_URL,
    acs_year: ACS_YEAR,
    variable: 'B19013_001E (median household income, past 12 months, inflation-adjusted)',
    units: 'USD',
    range: [lo, hi],
    generated: new Date().toISOString().slice(0, 10),
  };

  await mkdir(dirname(OUT_JSON), { recursive: true });
  await writeFile(OUT_JSON, JSON.stringify({ __meta: meta, ...out }, null, 0) + '\n');
  console.log(`Wrote ${OUT_JSON}`);

  const js = `// Auto-generated by scripts/build-county-income.mjs.
// DO NOT EDIT by hand — re-run the script to refresh.
// Median household income (USD) per US county FIPS, ACS 5-year ${ACS_YEAR}.
//
//   COUNTY_MEDIAN_INCOME["17031"] = 76916  // Cook County, IL
//
// Source: ${ACS_URL}

export const COUNTY_MEDIAN_INCOME = ${JSON.stringify(out)};
`;
  await writeFile(OUT_JS, js);
  console.log(`Wrote ${OUT_JS}`);
}

main().catch(err => { console.error(err); process.exit(1); });
